    <h2 align="center"><strong>PRESBYOPIA</strong></h2>
    <p>Presbyopia  is an age-related progressive loss of the focusing power of the lens. This  results in blurry close vision. The focusing power of the lens, which depends  upon the inherent elasticity of the lens, is gradually lost as people age.  Symptoms occur generally around the forties.<br>
      <br>
This results in a slow decrease in the ability of the  eye to focus on objects nearby. Presbyopia is a natural evolution of the aging  process and affects everyone :</p>
    <ul>
      <li><strong>Monovision</strong> and combined vision: Using PRK or Lasik surgery for <a href="myopia.html">myopia</a> and <a href="hyperopia.html">hyperopia</a>, and inducing a small degree of myopia on the dominated eye allows a comfortable close-up vision, while the dominating eye is corrected for far vision.</li>
      <li>	<strong>Presby-Lasik</strong>: <a href="refractive_surgery_lasik.html">Lasik surgery</a> that combines hyperopia correction for far vision and presbyopia for close vision on a second corneal focal. Visual outcome is usually 0.7 to 0.8 for far vision and 0.9 to 1.0 for close vision.</li>
      <li>	<strong>PRELEX</strong> (Presbyopic Lens exchange): Extraction by phacoemulsification of the opacified lens and insertion of a multi-focal or accommodative artificial lens, allowing far vision and near vision without glasses in 80% to 90% of cases. <br>
        In 10% to 20% of cases, glasses are needed for reading small characters in near vision. Limits and complications of PRELEX are similar to <a href="cataract_surgery.html">cataract surgery</a>.<br>
