    <p><a name="graft" id="graft"></a> </p>
    <p><strong>CORNEAL GRAFT or PENETRATING KERATOPLASTY (PKP):</strong><br>
    It’s the last solution for keratoconus in case of no-stabilization and failure of other techniques like rigid contact lenses, cross-linking or intra-ocular lenses and intra-corneal rings.</p>
    <p>      The purpose of corneal graft is to exchange the abnormal cornea by the cornea of a donor. It can be penetrating deep or lamellar graft on a part of the center cornea.<br>
      Deep lamellar or penetrating keratoplasty need sutures on the cornea at the end of the surgery. Sutures can be removed only after one year. Residual myopia and astigmatism after the removal of the sutures can be treated by Lasik and if they are very high, by Phakic intra-ocular lenses: <a href="intra-ocular_lenses_artisan.html">ARTISAN</a>, <a href="intra-ocular_lenses_artiflex.html">ARTIFLEX</a>, or <a href="intra-ocular_lenses_visian-ICL.html">TORIC ICL</a>.</p>
