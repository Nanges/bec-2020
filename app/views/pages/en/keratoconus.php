    <p>    <strong>PHAKIC  INTRA-OCULAR LENS</strong><br>
Phakic  intra-ocular lenses like <a href="intra-ocular_lenses_artisan.html">ARTISAN</a>, <a href="intra-ocular_lenses_artiflex.html">ARTIFLEX</a>, <a href="intra-ocular_lenses_visian-ICL.html">VISIAN ICL</a> and TORIC VISIAN ICL are  possible after stabilization of keratoconus by Corneal cross-linking with or  without Intra Corneal Rings to treat myopia and residual astigmatism.</p>
