    <p>Until 2002, existing diagnostic instruments could detect myopia, hyperopia and astigmatism. They represent 90% of all refractive errors. Higher order aberrations represent the last 10%.</p>
    <p>Aberrometers are new instruments able to measure and treat these higher order aberrations in a personal way specific to each patient.</p>
