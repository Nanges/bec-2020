    <p><strong>Front Wave Analysis:</strong><br>
    Originally used by astro-physicians, this technique is adapted to the treatment of refractive errors.</p>
    <ul>
      <li>	Higher order aberrations are measured by the analysis of the reflected image through an optic grid.</li>
      <li>The wave front diagnosis performed with a non-contact instrument that studies the whole optic system of the eye from the retina to the cornea.</li>
      <li>      Measurements of aberrations are combined to an analysis of the anterior and posterior corneal topography by the Orbscan.</li>
      <li>      Parameters measured by the Aberrometer are transferred to a disk. These data allow a computer-assisted treatment of common refractive errors (myopia and astigmatism) and higher order aberrations. This technique also prevents induced laser aberrations.</li>
      <li>	Advantages of a customized treatment by aberrometry are:
        <ul>
          <li>	Customized treatment according to aberrations for each eye.</li>
          <li>Enhancement of the best postoperative visual acuity sometimes up to more than 100%.</li>
          <li> Enhancement of night and sometimes day quality of vision compared to conventional treatment.</li>
          <li>Fewer risk of decrease of best corrected visual acuity.</li>
          <li>Better visual stability and fewer risk of re-treatment.</li>
          <li>Ability to treat severe ametropia not treatable until now by classic techniques, even with a moderate corneal thickness or a large pupil diameter at night. <br>
            </li>
        </ul>
      </li>
