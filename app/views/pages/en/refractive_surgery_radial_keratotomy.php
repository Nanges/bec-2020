    <h4>RADIAL KERATOTOMY</h4>
    <p>      Radial keratotomy was the first refractive surgery for correction of <a href="myopia.html">myopia</a>.</p>
    <ul>
      <li>	This technique was frequently used in the 70’s and 80’s before the beginning of the<a href="refractive_surgery_laser-Excimer.html"> Excimer Laser</a> treatment.</li>
      <li>In Radial Keratotomy, several incisions are performed from peripheral to central cornea.</li>
      <li>Numbers and length of incisions depend on the degree of myopia.</li>
      <li>The purpose of this surgery is to flatten the anterior cornea and focus the image back on the retina.</li>
      <li>Incisions are performed using a pre-calibrated diamond knife.</li>
      <li>Radial Keratotomy is indicated in low myopia and <a href="astigmatism.html">astigmatism</a>.</li>
      <li>Due to disadvantages of Radial Keratotomy compared to Excimer Laser treatment, this technique is almost not performed anymore.<br>
