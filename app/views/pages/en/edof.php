<h2 style="text-align:center;">EXTENDED DEPTH OF FOCUS IOL (EDOF)</h2>
<h3>Introduction</h3>
<p>Extended Depth of Focus (EDOF or EDF), or Extended Range of Vision, is a new technology that has recently emerged in the treatment of Presbyopia-correcting IOLs. In contrast to multifocal intraocular lenses (IOLs) used in treatment of presbyopia, EDOF lenses work by creating a single elongated focal point to enhance “range of vision” or “depth of focus”, without inducing night vision problems: glare or haloes.</p>
<h3>IsoPure, innovative EDOF monofocal hydrophobic preloaded IOL</h3>
<p>IsoPure G-Free ® is a fully refractive, aspherical and monofocal lens based on the unique Isofocal technology. The Isofocal technology provides cataract patients perfect far vision quality combined with visual improvement in intermediate vision by accentuating the extended depth of focus (EDOF) effect without inducing photopic visual phenomena or night vision problems.</p>


