    <h4>FEMTOLASIK - FEMTOSECOND LASER LDV Z6 (ZIEMER)</h4>
    <p> Femtolaser is a surgical device used to perform corneal lamellar surgery.<br>
    Doctor Assaf is using the Femto second laser LDV Z6 from the firm Ziemer. </p>
    <h4>Femstosecond laser applications are:</h4>
    <p>Refractive: lasik surgery for myopia, hyperopia, astigmatism, and presbyopia (corneal tunnel for inlays) </p>
    <p> Therapeutical: lamellar corneal graft, corneal tunnels for intracorneal rings for the treatment of keratoconus. <br>
    Other clinical indications are under studies. </p>
    <p>FEMTOLSECOND LASER is approved by the FDA and the clinical studies began in the 80s and the commercialisation began in the 90s. </p>
    <p>Femto second laser works with very short impulses of Femto second (10-15 second).  This very short impulse avoids thermal effect.  It allows, with the effect of electron on the atoms, the creation of several thousand of gas micro bubbles.  </p>
    <p>&nbsp;</p>
    <p align="center"><img src="../images/Z6.jpg" width="198" height="300" alt=""></p>
    <p>The surgeon is then able to lift the corneal flap with a spatula on the same level of the micro bubble gas layer. Femto second laser flaps are very precise with a defined diameter, thickness, and hinge length. It needs like the microkeratome the use of a suction ring during the lamellar surgery. </p>
    <p><strong> FEMTOSECOND versus Micro-Keratom:</strong><br>
    Clinical refractive results comparing flaps performed by Microkeratome and Femto second laser are not significantly different.  There are no clinical studies proving superiority of one technique compared to the other.  </p>
    <p>The personal experience of Dr. Assaf shows that Femto second has specific indications for thin corneas that are too steep or too flat and Microkeratome can be used in case of hyperopia to perform decentred flap because the visual axes of the hyperopic patients is already decentred. It is easier to perform decentred flap with Microkeratome than with Femto second laser.</p>
    <p>Microkeratome is also indicated in perfoming flaps after radial keratotomy. </p>
    <p>The main disadvantage of Femto second lasik compared to microkeratome is the cost (25% more expensive) because this technology is using a very expensive device and expensive disposables. </p>
    <p>The other disadvantage of Femtosecond Lasik is a longer time of suction on the eye to perform the flap. This could induce, as supported by some studies, more frequently dry eyes. </p>
