    <h4><strong>TRANS PRK</strong> </h4>
    <p>Schwind Amaris Laser specific technique&nbsp;:</p>
    <ul type="disc">
      <li>Epithelial cells abrasion by the laser</li>
	  <li>Followed by collagen abrasion with the same laser</li>
    </ul>
    <p>Advantages&nbsp;:</p>
    <ul type="disc">
      <li>Very quick treatment in few  seconds</li>
<li>Epithelial and collagen abrasion are transitional reducing  healing process time and post operative pain</li>
<li>This technique avoids mechanical abrasion and use of diluted alcohol on the epithelium</li>
<li>TRANS PRK is the only refractive surgery technique  where there is  no contact  at all with the eye; the laser performs all the steps of surgery.</li>
    </ul>
