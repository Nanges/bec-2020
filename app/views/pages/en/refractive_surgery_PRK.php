    <p><strong>LASER Excimer</strong></p>
    <h4><strong>PRK or PHOTOREFRACTIVE KERATECTOMY</strong> </h4>
    <p>	PRK was the first technique using Laser Excimer for correction of refractive errors.</p>
    <p>	PRK is performed in two steps:</p>
    <ol>
      <li>	Mechanical abrasion of the corneal epithelium</li>
      <li>	Laser computed-assisted treatment on the stroma underlying the removed epithelium.</li>
      <li>	Application of diluted mitomycine 0,02% in case of deep ablation to avoid sub-epithelial Haze.<br>
      </li>
    </ol>
    <p align="center"><img src="../images/prk.jpg" alt="PRK" width="250" height="242"><br>
    <span class="gws">    PRK -    Lasek </span></p>
    <ul type="disc">
      <li>	PRK induces ocular pain during 48 hours. Analgesics are necessary for few days.</li>
      <li>	Visual recovery needs one to two weeks.</li>
      <li>PRK is indicated for low and moderate refractive errors of myopia, hyperopia and astigmatism.</li>
      <li>Each eye is operated separately.<br>
      </li>
    </ul>
    
    <p>&nbsp;</p><p>&nbsp;</p>
