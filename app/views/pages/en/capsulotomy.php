<h3>Laser Yag Capsulotomy</h3>
<p>A posterior capsular opacification can occur after a cataract surgery. It is the opacification of
the posterio capsular where the intraocular lens is implanted. The treatment is performed by
laser Yag to open the visual axis.</p>
<p>This technic is performed with topical anesthesia in consultation. It takes a few minutes
without pain.</p>
<p>Floatters can appear after Laser Yag capsulotomie.</p>
<p>If lightning flashes are observed or an amputation of the visual field is noticed, an urgent
consultation should be performed to examine the retina, to exclude a retinal detachment
