    <h4><strong>LASIK</strong></h4>
    <p>      LASIK, short for Laser In Situ Keratomileusis, is the most frequently used technique by refractive surgeons in USA (92.3%) and Europe. Lasik is performed in two steps:</p>
    <p align="center"><img src="../images/lasik.jpg" alt="Lasik" width="250" height="251"><br>
      <span class="gws">Lasik</span></p>
    <ul>
      <li>		During the first step, a thin corneal flap is performed, using an automated micro-keratom or with a femtosecond laser.<br>
          <br>
      </li>
      <li>	The flap is then lifted and stays attached to the peripheral cornea.<br>
          <br>
      </li>
      <li>	Computer-assisted Excimer Laser is applied to the stroma underneath the lifted flap in order to shape the cornea according to the preoperative refraction error.<br>
          <br>
      </li>
      <li>	The flap is then re-applied without sutures.<br>
          <br>
      </li>
      <li>	Spontaneous adhesion occurs in few days.</li>
    </ul>
    <p>	There is no pain following surgery but itching can occur for few hours. Visual recovery is fast, usually in few hours as no epithelial abrasion is performed on the surface of the cornea.</p>
    <p>	Lasik indications are moderate and high degrees of refractive errors of <a href="myopia.html">myopia</a>, <a href="hyperopia.html">hyperopia</a> and <a href="astigmatism.html">astigmatism</a>.</p>
