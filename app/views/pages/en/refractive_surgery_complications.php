      •	Post-operative pain from 2 to 3 days <br>
      •	Late healing of the epithelium <br>
      •	Haze or sub-epithelial cicatrisation inducing a blur vision which decreases over time: 1% to 2%<br>
      •	Night halos if high myopia, high pupillary diameter or haze<br>
      •	Low risk of postoperative infection: 1/4000</p>
    <p><a href="refractive_surgery_lasik.html"><strong>      Lasik</strong></a><br>
      •	Decentration of the corneal flap during first post-operative days: patient must wear an eye-cover at night during 2 weeks after surgery<br>
      •	Dry eye: regular use of artificial tears is necessary during the first post-operative month<br>
      •	Epithelial ingrowth in the interface under the corneal flap (0.1%) avoided by rinsing<br>
      •	Inflammation under the corneal flap (0.5%) requiring anti-inflammatory treatment or rinsing the interface<br>
      •	Night halos if high myopia or large pupillary diameter<br>
      •	Extremely low risk of postoperative infection: 1/5000</p>
    <p><a href="refractive_surgery_aberrometry.html"><strong>Aberrometry:Customized wavefront treatment with aberrometry by Lasik or PRK:</strong></a><br>
      •	Same complications reported in Lasik<br>
      •	Less regression and retreatment (2%-3%). Usually, a retreatment free of charge is necessary in 4 to 5% of cases<br>
      Less Glare and night halos.</p>
