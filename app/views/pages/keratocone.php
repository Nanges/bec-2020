    <p><a name="keraring" id="keraring"></a></p>
    <p><strong>LES ANNEAUX INTRACORNEENS (KERARING)</strong><br>
    Il s'agit d'anneaux intracornéens en PMMA placés au niveau de la cornée la plus cambrée afin de réduire l'asigmatisme irrégulier. Selon les cas, un ou deux anneaux sont nécessaires.</p>
    <p>Cette technique chirurgicale permet l'applatissement de la cornée et favorise une meilleure acuité visuelle et une meilleure tolérance aux lentilles de contact. Cette technique chirurgicale est réalisable en ambulatoire sous anésthésie topique par gouttes.</p>
