    <p><strong>GREFFE DE CORNEE&nbsp;:</strong><br>
      Solution de dernier recours, en cas de kératocône très avancé ou de non stabilisation du kératocône et de l'échec de l'adaptation de lentilles semi-rigides, du Cross-Linking et de la possibilité de mise en place d'implants intraoculaires.</p>
    <p>      Il s’agit de remplacement de la cornée par un greffon.&nbsp; Cette technique peut être réalisée soit par  une greffe lamellaire ou une greffe transfixiante.</p>
    <p>      La greffe de cornée nécessite la mise en place de sutures cornéennes sur le  pourtour de la cornée. Ces points de suture ne peuvent être retirés qu’un an  après l’opération.&nbsp;</p>
