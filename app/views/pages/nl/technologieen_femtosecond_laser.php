    <h4>FEMTOSECOND LASER LDV (Ziemer)</h4>
    <p>      De Femtosecond laser is een nieuw chirurgisch apparaat, om corneasneden te maken.  Zijn toepassingsgebied is refractieve chirurgie (Lasik chirurgie van bij- verziendheid en astigmatisme) en therapeutische chirurgie : lamellaire corneagreffes, en het maken van corneatunnels voor het plaatsen van (INTACO) intracorneale ringen.<br>
      Andere klinische indicaties zijn nog in de experimentele fase.</p>
    <p>      Het gaat hier over een techniek die werd goedgekeurd in de Verenigde Staten (FDA Approval) waarbij de studies teruggaan tot begin jaren ’80 en de commercialisatie in de beginjaren ’90.</p>
    <p>      De Femtosecond laser werkt met extreem korte impulsen van de orde van een Femto seconde, het is te zeggen 10 -15 seconde.  Deze korte duur impuls vermijdt het thermische effect. Door de invloed op de elektronen van de atomen worden duizenden kleine gasbelletjes gevormd.  Zo kan de chirurg de snede opheffen met een pincet dat dwars door het actieniveau van de laser gaat namelijk door het niveau van microballetjes.</p>
    <p>      Het snijden met de Femtolaser laat toe op voorhand de diameter en de dikte van het snijvlak te bepalen alsook de lengte van het scharniervlak.  Zoals bij het microkeratoom is hier ook een zuigring nodig tijdens het snijden.<br>
      De indicaties zijn een te kromme of te platte cornea.</p>
    <p>      Dr. Assaf gebruikt de femtosecond LDV van de firma Ziemer.<br>
      FEMTOSECOND in vergelijking met het microkeratoom:<br>
      De refractieve resultaten van de Lasik met het microkeratoom of de femtosecond zijn niet significant verschillend.</p>
    <p>      Op dit moment is er geen enkele klinische studie die de superioriteit van de ene methode ten opzichte van de andere methode kan aantonen.<br>
      Nochtans, dankzij de persoonlijke ervaring van Dr. Assaf, zou de Femtosecond beter zijn bij te kromme of te platte hoornvliezen, of te kleine ooglidspleet, en het microkeratoom zou beter zijn bij de hypermetroop, de visuele as van de hypermetroop is altijd gedecentreerd.  Het is gemakkelijker om  een gedecentreerde snede  te maken met het microkeratoom dan met de Femtosecond laser.</p>
    <p>      Het grootste nadeel van de Lasik techniek met de Femtosecond laser is hoge kostprijs (25% duurder dan Lasik met het microkeratoom) omdat deze techniek gebruikmaakt van duur wegwerpmateriaal, ook de femto Second Laser zelf is zeer  duur.  Een ander nadeel is de langere duur van de suctie en de langere duur om het snijvlak  te maken.  Daardoor zou, volgens bepaalde studies, deze laser techniek veel frequenter een droge cornea geven.</p>
