    <h2 align="center"><strong>KERATOCONONUS</strong></h2>
    <p>
    Dit is een evolutieve ziekte van de cornea, waarvan de eerste tekens rond de leeftijd van 20 jaar verschijnen en die een kegelvormige uitstulping van de cornea geven met een onregelmatig astigmatisme geassocieerd aan een myopie of bijziendheid.</p>
    <p>      <strong>HALF-HARDE CONTACTLENZEN</strong> :<br>      
    Dit zijn lenzen die de corneaal astigmatisme kunnen afvlakken.  Het zijn ofwel de klassieke half–harde lenzen of speciaal voor keratoconus gepersonaliseerd. De aanpassing moet regelmatig opgevolgd worden.</p>
    <p><a name="CrossLinking" id="CrossLinking"></a></p>
    <p>      <strong>CORNEALE CROSS-LINKING</strong> :<br>
      Dit is een techniek die het harder maken van de cornea viseert door bevriezing met behulp van ultra-violet licht, om zo het uitstulpen van de cornea te doen stoppen.<br>
      Het principe van de behandeling is het toepassen van ultra-violet stralen op het indruppelen van riboflavine om zo een fotopolymerisatie van het collageen te bewerkstellingen.</p>
    <p>      De Cornea Cross Linking bestaat uit het indruppelen van riboflavine en de toepassing van ultra-violet licht na een mechanische of een door Excimer laser deëpithelialisatie van de cornea.  Deze interventie kan onder lokale druppelanesthesie gebeuren.</p>
    <p>      Een lichte hinder of een lichte pijn kunnen gedurende 24 tot 48 uur na de ingreep voorkomen.  In sommige gevallen is de corneale Cross Linking geassocieerd aan een behandeling van de corneaoppervlak door Excimer laser begeleid door corneal topografie (PRK of PTK) : TOPOGuided treatment.</p>
    <p align="center"><img src="../images/cornealcrosslinking.jpg" width="305" height="245"></p>
