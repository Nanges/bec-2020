    <p><strong>CORNEAGREFFE</strong> :<br>
    Dit is de allerlaatste oplossing, als er geen stabilisatie van de keratoconus is en de aanpassing van semi-rigide contactlenzen mislukt, alsook de Cross-Linking en de mogelijkheid van het implanten van intraoculaire implantaten.  </p>
    <p>Het is de vervanging van de cornea door een greffe.  Deze techniek kan gerealiseerd worden door een lamellaire greffe of een transfixerende greffe.</p>
