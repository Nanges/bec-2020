    <p><strong>INTRA-CORNEALE RINGEN (INTRA-OCULAR RINGS)</strong><br>
      Deze intra-corneale ringen zijn ringen vervaardigd uit PMMA die in het achterste derde van de cornea worden geplaatst om het meest gekromde deel van de cornea af te platten, namelijk de keratoconus, die het onregelmatige astigmatisme geeft.</p>
    <p>      Deze chirurgische interventie wordt ambulatoir gedaan onder topische druppelanesthesie en ze kan gecombineerd worden met Corneal Cross Linking.  Ze kan het astigmatisme reduceren en zo een betere visus geven met behulp van een bril of contact lenzen.</p>
