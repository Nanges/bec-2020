    <h4>Temps de traitement minimal</h4>
    <p>Le Laser SCHWIND AMARIS 750S - 1050 hz ET travaille à une vitesse de deux à trois fois plus rapide que la moyenne des autres lasers. 750 petits points lumineux par seconde sculptent la surface cornéenne. Il est très rapide et corrige une dioptrie de myopie en 1,5 seconde et huit dioptries sont traitées en treize secondes.</p>
    <h4>Ajustement automatique de l’énergie</h4>
    <p>Afin de combiner parfaitement vitesse et précision, le laser SCHWIND AMARIS 750S - 1050 hz ET est équipé d’une fréquence d’émission de 750 Hz avec deux niveaux d’énergie.
Le laser commence par un niveau d’énergie élevé qui ablate à peu près 80% de l’anomalie réfractive avec un contrôle permanent de l’énergie délivrée pour votre anomalie réfractive. Les 20% du traitement restant seront ablatés par une énergie plus faible afin d’assurer un lissage de la cornée.</p><p>
Ceci est comparable à un bateau qui s’approche à grande vitesse au port et qui laisse ensuite partir un petit bateau, lequel naviguera dans des endroits plus étroits, avec une plus grande précision, pour attendre le but
</p>
    <h4>Système de poursuite de l’oeil par infrarouge en six dimensions (Eye Tracker)</h4>
    <p>Les yeux peuvent, de manière involontaire, bouger dans toutes les directions durant quelques millisecondes pendant la fixation du laser. Le système de poursuite du Laser SCHWIND AMARIS 750S - 1050 hz ET s’adapte en permanence à la position de l’oeil avec approximativement 1050 mesures par seconde et compense ainsi toutes les déviations possibles dans les six dimensions.</p>
    <p>L’oeil est comparable à une bouée sur la surface de l’eau.</p>
    <p>Quand la mer est calme, la bouée peut subir des mouvements de la gauche vers la droite ou de l’avant vers l’arrière. Quand la mer est plus agitée, la bouée peut se dévier vers la droite ou la gauche, tourner autour d’elle-même et subir des mouvements vers le haut et vers le bas. De la même manière, le globe oculaire peut subir des rotations sur son propre axe (cyclotorsion) ou bouger en haut ou en bas le long de l’axe Z.</p><p>
Le système de poursuite par Laser SCHWIND AMARIS 750S - 1050 hz ET détecte tous ces mouvements et les compense. Il assure ainsi une poursuite permanente du centre visuel de l’oeil. Concernant le mouvement de la cyclotorsion, la compensation a eu lieu de manière statique lors des mesures préopératoires afin de compenser ces mouvements entre la position assise et allongée.</p><p>
Les mouvements de cyclotorsion dynamiques, qui ont lieu pendant le traitement, sont compensés en permanence par le Eye Tracker du laser qui les compense dynamiquement.
</p>
    <h4>Mesure permanente de l’épaisseur de la cornée</h4>
    <p>Comparable à un plongeur sous-marin, qui peut mesurer la profondeur à laquelle il se trouve par un profondimètre, le médecin est informé en permanence et en temps réel du niveau auquel le laser travaille par le pachymètre du SCHWIND AMARIS 750S - 1050 hz ET.</p><p>
Ce système mesure les changements de l’épaisseur cornéenne en temps réel et les projette sur l’écran. De ce fait, le chirurgien sait exactement et à tout moment à quel niveau de profondeur le laser est en train d’ablater et quel est le tissu résiduel.
</p>
    <h4>Un faisceau laser exceptionnellement fin </h4>
    <p>Le faisceau laser du SCHWIND AMARIS 750S - 1050 hz ET a un diamètre exceptionnellement petit de seulement 0,54 mm ; ce qui est significativement plus petit que les autres spots disponibles sur le marché.<br> 
Avec ce faisceau très fin, la cornée peut être remodelée de manière très régulière, et la surface cornéenne peut être égalisée, lissée, de manière très homogène.
</p>
    <h4>Microclimat constant</h4>
    <p>Le SCHWIND AMARIS 750S - 1050 hz ET intègre un dispositif d’évacuation des débris cellulaires (nozzles). Installé juste au-dessus des yeux à opérer, il assure un microclimat stable et permet l’évacuation des débris cellulaires au-dessus de la cornée.<br>
Ces conditions climatiques constantes préviennent le dessèchement de la cornée et assurent un meilleur résultat post-opératoire.
</p>
    <h4>Le contrôle thermique unique</h4>
    <p>Le SCHWIND AMARIS 750S - 1050 hz ET est équipé d’un système de contrôle thermique unique. Même à une vitesse d’ablation très rapide, le tissue cornéen est préservé efficacement. Les pulsations du laser sont séquentielles et réparties dans l’espace-temps, ce qui assure à la cornée un temps suffisant pour se refroidir entre chaque pulsation. L’arrivée de la pulsation suivante a lieu sur une position de la cornée déjà refroidie.</p>
    <h4>Résultats cliniques</h4>
    <p>De multiples études cliniques internationales ont documenté les excellents résultats postopératoires et la qualité visuelle obtenue avec le Laser SCHWIND AMARIS 750S - 1050 hz ET. 
L’acuité visuelle de 100% a été obtenue dans la quasi totalité des cas.</p><p>
