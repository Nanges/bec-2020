    <h4><strong>PRK ou  PHOTOKERATECTOMIE REFRACTIVE</strong> </h4>
    <p>Il s'agit de la première technique         utilisant le Laser Excimer, pour le traitement des défauts de la vision.</p>
    <p>Cette technique chirurgicale         comporte trois temps opératoires :</p>
    <ol start="1" type="1">
      <li>Pelage mécanique de l'épithélium cornéen, avec une brosse ou un scarificateur, avec ou sans alcool dilué.</li>
      <li>Application du faisceau laser,         assisté par ordinateur, sur la cornée sous-jacente à l'épithélium.</li>
      <li>Application de la mitomycine diluée         0,02% en cas d’ablation profonde pour éviter la cicatrisation sous-épithéliale         (Haze).</li>
    </ol>
    <p align="center"><img src="images/prk.jpg" alt="PRK" width="250" height="242"><br>
    <span class="gws">    PRK -    Lasek </span></p>
    <ul type="disc">
      <li>L'abrasion des cellules         superficielles de la cornée entraîne une douleur postopératoire de quelques jours nécessitant un traitement         par antidouleurs.</li>
      <li>La récupération visuelle est lente ;         une à deux semaines.</li>
      <li>Cette technique est réservée aux         degrés moyens des défauts de la vision : myopie, hypermétropie,         astigmatisme.</li>
      <li>Les deux yeux sont opérés séparément, ou simultanément</li>
    </ul>
    <p>&nbsp;</p>
