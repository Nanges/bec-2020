    <h4><strong>TRANS PRK</strong> </h4>
    <p>Technique propre au laser Excimer Schwind Amaris&nbsp;:</p>
    <ul type="disc">
      <li>Abrasion des cellules épithéliales de surface par le laser</li>
		<li>Suivi par une abrasion du collagène par le même laser Excimer</li>
    </ul>
    <p>Les avantages de cette technique sont les suivants&nbsp;:</p>
    <ul type="disc">
      <li>Rapidité&nbsp;: traitement en quelques secondes</li>
<li>Proximité de l'ablation épithéliale et de l'ablation du collagène favorisant une cicatrisation plus rapide et diminution de la période douloureuse postopératoire</li>
<li>Cette technique évite l'ablation mécanique et l'utilisation de l'alcool dilué sur l'épithélium</li>
<li>Il s'agit de la seule technique chirurgicale sans aucun contact avec la cornée, le laser faisant lui-même toutes les étapes de la chirurgie</li>
    </ul>
