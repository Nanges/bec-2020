<h2 class='text-center'>LA CHIRURGIE RÉFRACTIVE</h2>
<h3 class="fs-12">S.M.I.L.E (SMall Incision Lenticule Extraction)</h3>
<p>Il s’agit d’une nouvelle technique chirurgicale de la myopie et de l’astigmatisme par Femto
Second laser. Elle s’effectue sous anesthésie locale par gouttes.</p>
<p>Le laser prédécoupe un petit lenticule dans l’épaisseur de la cornée correspondant au volume
de la myopie et de l’astigmatisme à traiter.</p>
<p>Le laser réalise une petite incision périphérique (de 2 à 4 mm de largeur) à travers laquelle le
chirurgien va disséquer le lenticule et de l’extraire de la cornée.</p>
<p>Les résultats de cette technique dans le cas de la myopie et de l’astigmatisme sont
prédictibles et entrainent de très bons résultats réfractifs.</p>

<h4 class='fs-12'>AVANTAGES<!-- de la technique SMILE par rapport au Lasik et autres techniques--></h4>
<ul>
	<li>Absence de réalisation d’un capot fragile en cas de frottements et de traumatisme.</li>
	<li>Moindre sécheresse</li>
</ul>

<h4 class='fs-12'>INCONVENIENTS<!--de la technique SMILE par rapport au Lasik--></h4>
<ul>
	<li>Récupération visuelle plus lente.</li>
	<li>Vision optimale après quelques jours et non pas le lendemain comme le Lasik.</li>
	<li>Nécessité de traitement anti-inflammatoires de plus longue durée.</li>
	<li>Le SMILE contrairement au Lasik ne traite pas pour le moment les forts astigmatismes et l’hypermétropie.</li>
	<li>Difficulté de retraitement en cas de sur et de sous correction.</li>
</ul>

<h4 class='fs-12'>COMPLICATIONS</h4>
<p>Les complications sont identiques aux chirurgies réfractives par Laser (infections,
inflammation, troubles de vision nocturnes…) hormi les plis et le déplacement du capot.</p>

