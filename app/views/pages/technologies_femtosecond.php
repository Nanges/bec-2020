    <h4>FEMTOLASIK par FEMTOSECOND LASER LDV Z6 (ZIEMER)</h4>
    <p>Le laser Femtosecond est un outil chirurgical destiné aux découpes de la cornée.<br>
    Le Docteur Assaf utilise le femtosecond LDV Z6 de la firme Ziemer.    </p>
    <p>      Ses applications sont : <br>
      D'ordre réfractif : chirurgie par Lasik de la myopie, de l'hypermétropie, de l'astigmatisme, presbytie (tunnel cornéen et inlay cornéen) <br>
      D'ordre thérapeutique : greffe lamellaire de cornée, réalisation de tunnels cornéens pour la mise en place d'anneaux intra-cornéens pour le traitement du kératocône. <br>
    D'autres indications cliniques sont en cours d'étude. </p>
    <p>Il s'agit d'une technologie approuvée aux Etats-Unis (FDA approval) dont les études remontent au début des années 80 et la commercialisation début des années 90. </p>
    <p>Le Femtosecond Laser travaille avec des impulsions extrêmement brèves, de l'ordre de la Femto seconde soit 10<sup>-15</sup> seconde. Cette faible durée d'impulsion permet d'éviter les effets thermiques. Il permet, grâce à un effet sur les électrons des atomes, la création de plusieurs milliers de petites bulles de gaz. Le soulèvement du capot est alors possible par le chirurgien grâce à une pince qui traverse le plan de l'action du laser au niveau des microbulles. </p>
    <p>&nbsp;</p>
    <p align="center"><img src="images/Z6.jpg" width="198" height="300" alt=""></p>
    <p>&nbsp;</p>
<p>La découpe au Femtosecond Laser permet de prédéterminer le diamètre du capot, l'épaisseur du capot ainsi que la longueur de la charnière. Il nécessite comme le microkératome la mise en place d'un anneau de succion hors de la découpe. </p>
    <p>Ses indications sont privilégiées en cas de cornée trop cambrée ou trop plate.</p>
    <p> FEMTOSECOND versus Microkératome : Le résultat réfractif suite aux Lasik réalisés tant par Microkératome que par Femtosecond n'est pas significativement différent. A ce jour aucune étude clinique ne démontre la supériorité de l'une par rapport à l'autre méthode. Néanmoins, par l'expérience personnelle du Docteur Assaf, le Femtosecond pourrait avoir des indications privilégiées dans les cornées trop plates ou trop cambrées, et les petits écarts palpébraux, et le Microkératome en cas d'hypermétropie pour réaliser un volet décentré car l'axe visuel des hypermétropes est toujours décentré. Il est plus facile de réaliser un capot décentré par Microkératome que par femtosecond. </p>
    <p>Le microkératome est indiqué également dans la réalisation de capot après kératotomie radiaire.</p>
    <p>L'inconvénient majeur de la technique Lasik par Femtosecond est son coût qui est relativement plus élevé (25% plus cher que le Lasik par Microkératome) car cette technologie utilise du matériel disposable coûteux et le prix du laser Femtosecond est très élevé. Son autre inconvénient est la durée plus longue de la succion et de la réalisation du capot. De par ce fait, il induirait selon certaines études plus fréquemment une sécheresse cornéenne.</p>
