<h2 align="center"><strong>LA CATARACTE</strong> SECONDAIRE</h2>
<h3>Capsulotomie Laser Yag</h3>
<p>Une cataracte secondaire peut survenir après une chirurgie de la cataracte avec mise en
place d’un implant intraoculaire. Il s’agit d’une opacification de la capsule postérieure sur
laquelle repose l’implant intraoculaire par phénomène de fibrose ou de multiplication
cellulaire.</p>
<p>le traitement de la cataracte secondaire se fait par un laser Yag afin de dégager l’axe visuel.
Cela est pratiqué en consultation en ambulatoire en quelques minutes, sans contact avec
l’œil et sans douleur.</p>
<p>Il est possible que survienne après la capsulotomie au laser Yag la vision de corps flottants
dans le champ de vision. Exceptionnellement, un décollement de rétine peut survenir avec
des signes d’éclats lumineux de corps flottants sous forme de suif ou d’une amputation du
champ de vision. Ceci devra inciter à une consultation en urgence.</p>


