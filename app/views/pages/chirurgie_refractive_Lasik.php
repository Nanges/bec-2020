    <p align="center"><img src="images/lasik.jpg" alt="Lasik" width="250" height="251"><br>
      <span class="gws">Lasik</span></p>
    <ul>
      <li>	Le Lasik ou Laser In Situ Keratomileusis débute par la réalisation d'une fine lamelle cornéenne superficielle (capot)</li>
      <li>	Ce capot est réalisé grâce à un microkératome automatisé ou à un Femtosecond laser</li>
      <li>Il est ensuite soulevé et reste attaché à une partie de la périphérie cornéenne</li>
      <li>Le faisceau laser Excimer assisté par ordinateur est appliqué sur la partie sous-jacente</li>
      <li>Le capot est ensuite repositionné, sans sutures</li>
      <li>L'adhérence se fait spontanément en quelques jours</li>
    </ul>
    <p> Les suites opératoires sont indolores et la récupération visuelle est rapide, en quelques heures, car aucune abrasion n'est réalisée sur la surface de l'œil.  </p>
    <p>Le Lasik s'adresse aux degrés moyens et forts des défauts de la vision : <a href="myopie.html">myopie</a>, <a href="hypermetropie.html">hypermétropie</a>, <a href="astigmatisme.html">astigmatisme</a>.</p>
