    <p>      Depuis 2008, <a href="ophtalmologues_bruxelles/docteur_jean_assaf.html">le Docteur Assaf</a> réalise également, les capots cornéens par le Femtosecond laser LDV : cornées trop cambrées ou trop plates, cornées trop fines...</p>
    
