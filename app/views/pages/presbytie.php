    <h2 align="center"><strong>LA PRESBYTIE</strong></h2>
    <p>La presbytie est un  trouble de l'accommodation induit par une diminution de l'élasticité du  cristallin et qui apparaît physiologiquement après la quarantaine.</p>
    <p>      Elle se traduit par la difficulté de mise au point en vision de près.</p>
    <p>      Le traitement  chirurgical de la presbytie est possible dans certains cas, par différentes  techniques :</p>
    <ul>
      <li><strong>Technique de la bascule</strong> : Grâce au       laser Excimer PRK ou Lasik-Femtolasik lors du traitement de la myopie et de l'hypermétropie, on peut induire une       légère myopie sur l'œil non directeur, permettant un confort visuel       rapproché sans lunettes, associé au traitement de l'œil directeur pour la       vision éloignée. Technique réversible en cas de qualité visuelle inconfortable.</li>
      <li><strong>Presby-Lasik</strong> : il s'agit d'un       traitement par Lasik, associant au traitement de l'hypermétropie pour la       vision de loin, un traitement pour la presbytie de près, sur une deuxième       focale cornéenne. Cette technique permet d'obtenir une vision sans       correction en vision éloignée, de 7 à 8/10 et de 10/10 en vision       rapprochée.</li>
      <li><strong>PresbyMax</strong>&nbsp;: il s'agit d'un traitement incorporé au laser Schwind Amaris et qui permet un traitement assisté par ordinateur de la presbytie associée à la myopie, l'hypermétropie ou l'astigmatisme en créant une multifocalité cornéenne octroyant une vision éloignée et rapprochée sans correction. Cette technique est réversible en cas de troubles visuels nocturnes gênants.<br><br>
