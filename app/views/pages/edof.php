<h2 style="text-align:center">IMPLANT INTRAOCULAIRE (IOL) avec profondeur de champ (EDOF)</h2>
<h3>Introduction</h3>
<p>L’implant intraoculaire avec profondeur de champ étendue (EDOF) utilise une nouvelle technologie qui a été récemment mise sur le marché pour la correction de la presbytie par les implants intraoculaires.</p>
<p>Contrairement aux implants oculaires multifocaux, utilisés pour le traitement de la presbytie, l’implant EDOF fonctionne en créant un point de focalisation étendu pour améliorer la profondeur de champ et l’étendue du champ visuel permettant ainsi une vision sans correction pour la vision éloignée et intermédiaire, sans troubles visuels nocturnes : halos, ou éblouissements par la lumière.</p>
<h3>ISOPURE, monofocal, hydrophobe, préchargé, implant ou IOL bénéficiant de la nouvelle technique inovatrice EDOF</h3>
<p>L’implant IsoPure G-Free est un implant totalement réfractif, asphérique et monofocal basé sur la technique ISOfocale. Cette technologie octroie au patient bénéficiant d’une opération de la cataracte une parfaite vision sans correction de loin, et en vision intermédiaire.</p>
<p>Cette technologie accentue le champ de profondeur sans entrainer de troubles nocturnes comme les implants multifocaux.</p>



